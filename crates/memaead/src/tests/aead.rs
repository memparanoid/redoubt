#[test]
fn test_modified_tag_rejected() {
    let key = [0x42u8; 32];
    let xnonce = [0x24u8; 24];
    let aad = b"header";
    let plaintext = b"secret";

    let mut ciphertext = xchacha20poly1305_encrypt(&key, &xnonce, aad, plaintext);

    // Flip one bit in the tag
    let last = ciphertext.len() - 1;
    ciphertext[last] ^= 0x01;

    // Should fail authentication
    assert_eq!(
        xchacha20poly1305_decrypt(&key, &xnonce, aad, &ciphertext),
        Err(DecryptError::AuthenticationFailed)
    );
}

#[test]
fn test_modified_ciphertext_rejected() {
    let key = [0x42u8; 32];
    let xnonce = [0x24u8; 24];
    let aad = b"header";
    let plaintext = b"secret";

    let mut ciphertext = xchacha20poly1305_encrypt(&key, &xnonce, aad, plaintext);

    // Flip one bit in the ciphertext (not tag)
    ciphertext[0] ^= 0x01;

    // Should fail authentication
    assert_eq!(
        xchacha20poly1305_decrypt(&key, &xnonce, aad, &ciphertext),
        Err(DecryptError::AuthenticationFailed)
    );
}

#[test]
fn test_modified_aad_rejected() {
    let key = [0x42u8; 32];
    let xnonce = [0x24u8; 24];
    let plaintext = b"secret";

    let ciphertext = xchacha20poly1305_encrypt(&key, &xnonce, b"header", plaintext);

    // Different AAD should fail
    assert_eq!(
        xchacha20poly1305_decrypt(&key, &xnonce, b"HEADER", &ciphertext),
        Err(DecryptError::AuthenticationFailed)
    );
}

#[test]
fn test_roundtrip() {
    let key = [0x42u8; 32];
    let xnonce = [0x24u8; 24];
    let aad = b"associated data";
    let plaintext = b"Hello, XChaCha20-Poly1305!";

    let ciphertext = xchacha20poly1305_encrypt(&key, &xnonce, aad, plaintext);
    let decrypted = xchacha20poly1305_decrypt(&key, &xnonce, aad, &ciphertext).unwrap();

    assert_eq!(decrypted, plaintext);
}

#[test]
fn test_empty_plaintext() {
    let key = [0x42u8; 32];
    let xnonce = [0x24u8; 24];
    let aad = b"just aad, no plaintext";

    let ciphertext = xchacha20poly1305_encrypt(&key, &xnonce, aad, b"");
    assert_eq!(ciphertext.len(), TAG_SIZE); // Only tag

    let decrypted = xchacha20poly1305_decrypt(&key, &xnonce, aad, &ciphertext).unwrap();
    assert!(decrypted.is_empty());
}

